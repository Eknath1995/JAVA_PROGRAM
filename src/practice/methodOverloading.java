package practice;

public class methodOverloading {
		void display(int a) {
			System.out.println("a : " + a);
		}
		
		void display() {
			System.out.println(" hello world");
		}
		
		void display(double a) {
			System.out.println("height :"+ a);
		}
		
		void display(int x, String y) {
	System.out.println( "x :"+ x+ " y :"+y);
		}
}
